/**
 * Math Demo. In Java there is a Math class.
 */
public class MathApp {
    public static void main(String[] args) {
        System.out.println("Το μικρότερο μεταξύ του 3 και του 5 είναι: " + Math.min(3, 5));
        System.out.println("Το μεγαλύτερο μεταξύ του 3 και του 5 είναι: " + Math.max(3, 5));
    }
}
